func lengthOfLongestSubstring(s string) int {
	if len(s) <= 0 || int(5*math.Pow(10, 4)) <= len(s) {
		return 0
	}
	currentsubString := ""
	for i := range s {
		substr := ""
		for k := i; k < len(s); k++ {
			if strings.Contains(substr, string(s[k])) {
				break
			}
			substr += string(s[k])
		}
		if len(substr) > len(currentsubString) {
			currentsubString = substr
		}
	}
	return len(currentsubString)
}
